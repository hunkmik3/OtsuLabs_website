CREATE TABLE IF NOT EXISTS {{CMS_QUALIFIED_REDIRECT_TABLE}} (
    id TEXT PRIMARY KEY,
    from_path TEXT NOT NULL UNIQUE,
    to_path TEXT NOT NULL,
    status_code INTEGER NOT NULL CHECK (status_code IN (301, 302)),
    created_at TIMESTAMPTZ NOT NULL,
    updated_at TIMESTAMPTZ NOT NULL
);

CREATE INDEX IF NOT EXISTS {{CMS_REDIRECT_FROM_INDEX}} ON {{CMS_QUALIFIED_REDIRECT_TABLE}} (from_path);
CREATE INDEX IF NOT EXISTS {{CMS_REDIRECT_TO_INDEX}} ON {{CMS_QUALIFIED_REDIRECT_TABLE}} (to_path);
